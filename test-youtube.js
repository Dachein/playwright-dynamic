const { chromium } = require('playwright');

async function testYouTubeExtraction() {
  console.log('üé¨ Starting YouTube extraction test...\n');
  
  const browser = await chromium.launch({
    headless: true, // Êó†Â§¥Ê®°Âºè
    args: [
      '--no-sandbox',
      '--disable-setuid-sandbox',
      '--disable-dev-shm-usage',
      '--disable-blink-features=AutomationControlled'
    ]
  });
  
  const context = await browser.newContext({
    userAgent: 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 Chrome/120.0.0.0 Safari/537.36',
    viewport: { width: 1920, height: 1080 },
    ignoreHTTPSErrors: true
  });
  
  const page = await context.newPage();
  
  try {
    const videoUrl = 'https://www.youtube.com/watch?v=y2NeAef6d30';
    console.log(`üì∫ Loading: ${videoUrl}\n`);
    
    await page.goto(videoUrl, {
      waitUntil: 'domcontentloaded',
      timeout: 30000
    });
    
    // Á≠âÂæÖÈ°µÈù¢Âä†ËΩΩ
    await page.waitForTimeout(5000);
    
    console.log('‚úÖ Page loaded, extracting data...\n');
    
    // ÊèêÂèñ ytInitialPlayerResponse
    const result = await page.evaluate(async () => {
      const pr = window.ytInitialPlayerResponse;
      if (!pr) {
        return { error: 'ytInitialPlayerResponse not found' };
      }
      
      const vd = pr.videoDetails || {};
      const mf = pr.microformat?.playerMicroformatRenderer || {};
      
      // Âü∫Êú¨ÂÖÉÊï∞ÊçÆ
      const metadata = {
        title: vd.title,
        author: vd.author,
        channelId: vd.channelId,
        videoId: vd.videoId,
        lengthSeconds: vd.lengthSeconds,
        viewCount: vd.viewCount,
        publishDate: mf.publishDate || mf.uploadDate,
        ownerChannelName: mf.ownerChannelName,
        description: vd.shortDescription?.substring(0, 200) + '...'
      };
      
      // Ê£ÄÊü•Â≠óÂπï
      const captions = pr.captions?.playerCaptionsTracklistRenderer;
      const captionInfo = {
        hasCaptions: !!captions,
        tracksCount: captions?.captionTracks?.length || 0,
        tracks: []
      };
      
      if (captions?.captionTracks) {
        captionInfo.tracks = captions.captionTracks.map(t => ({
          languageCode: t.languageCode,
          kind: t.kind || 'manual',
          name: t.name?.simpleText || t.name?.runs?.[0]?.text,
          baseUrl: t.baseUrl?.substring(0, 100) + '...' // Âè™ÊòæÁ§∫Ââç100Â≠óÁ¨¶
        }));
        
        // Â∞ùËØïËé∑ÂèñÁ¨¨‰∏Ä‰∏™Â≠óÂπïËΩ®ÈÅìÁöÑÂÜÖÂÆπ
        if (captions.captionTracks.length > 0) {
          const track = captions.captionTracks[0];
          const transcriptUrl = track.baseUrl + '&fmt=json3';
          
          try {
            console.log('Fetching transcript from:', transcriptUrl.substring(0, 100));
            const res = await fetch(transcriptUrl);
            const data = await res.json();
            
            // Ëß£ÊûêÂ≠óÂπïÊñáÊú¨
            let text = '';
            for (const event of (data.events || [])) {
              for (const seg of (event.segs || [])) {
                if (seg.utf8) text += seg.utf8;
              }
            }
            
            captionInfo.transcript = {
              language: track.languageCode,
              isAutoGenerated: track.kind === 'asr',
              textLength: text.length,
              preview: text.substring(0, 200) + '...'
            };
          } catch (e) {
            captionInfo.transcript = {
              error: e.message
            };
          }
        }
      }
      
      return {
        success: true,
        metadata,
        captions: captionInfo
      };
    });
    
    console.log('üìä Extraction Result:\n');
    console.log(JSON.stringify(result, null, 2));
    
    if (result.success && result.captions.transcript) {
      console.log('\n‚úÖ Successfully extracted transcript!');
      console.log(`   Language: ${result.captions.transcript.language}`);
      console.log(`   Auto-generated: ${result.captions.transcript.isAutoGenerated}`);
      console.log(`   Text length: ${result.captions.transcript.textLength} chars`);
    } else if (result.captions.hasCaptions) {
      console.log('\n‚ö†Ô∏è  Captions available but transcript fetch failed');
      console.log(`   Error: ${result.captions.transcript?.error || 'Unknown'}`);
    } else {
      console.log('\n‚ùå No captions available for this video');
    }
    
  } catch (error) {
    console.error('\n‚ùå Error:', error.message);
  } finally {
    await browser.close();
  }
}

testYouTubeExtraction().catch(console.error);
